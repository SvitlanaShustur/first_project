# Ви придбали товар на певну суму s. Скільки купюр різного номіналу треба віддати продавцю, якщо починати платити з найбільших? У вас є 1, 2, 5, 10, 100, 500 гри
s = int(input("Введіть суму покупки"))
s_500 = s // 500
s = s % 500

s_100 = s // 100
s = s % 100

s_10 = s // 10
s = s % 10

s_5 = s // 5
s = s % 5

s_2 = s // 2
s = s % 2

s_1 = s // 1
s = s % 1

print(s_500)
print(s_100)
print(s_10)
print(s_5)
print(s_2)
print(s_1)